package ejercicio1;

public class Ejercicio1 {
    public static void main(String[] args) {
        // declaración de variables
        int contador = 1;

        /* mostramos el número por pantalla y le añadimos una unidad hasta que llegue a 20*/
        do {
            System.out.println(contador);
            contador++;
        }while (contador <= 20);
    }
}